function translateFrenchDate(value: string) {
  const months: Record<string, string> = {
    janvier: 'January',
    'janv.': 'Jan',
    février: 'February',
    'févr.': 'Feb',
    mars: 'March',
    avril: 'April',
    'avr.': 'Apr',
    mai: 'May',
    juin: 'June',
    juillet: 'July',
    'juil.': 'Jul',
    août: 'August',
    septembre: 'September',
    'sept.': 'Sep',
    octobre: 'October',
    'oct.': 'Oct',
    novembre: 'November',
    'nov.': 'Nov',
    décembre: 'December',
    'déc.': 'Dec',
  }

  return Object.entries(months).reduce(
    (translated, [french, english]) => translated.replace(new RegExp(french.replace('.', '\\.'), 'gi'), english),
    value,
  )
}

