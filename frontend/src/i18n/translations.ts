export function translateToEnglish(value: string): string | null {
  const paragraphs = value.split(/\n\s*\n/)
  if (paragraphs.length > 1) {
    const translatedParagraphs: Array<string | null> = paragraphs.map((paragraph) => translateToEnglish(paragraph))
    if (translatedParagraphs.every((paragraph): paragraph is string => paragraph !== null)) {
      return translatedParagraphs.join('\n\n')
    }
  }

  const normalized = value.replace(/\s+/g, ' ').trim()
  if (!normalized) return null
  if (englishTranslations[normalized]) return englishTranslations[normalized]

  for (const [pattern, replacement] of englishPatterns) {
    const match = normalized.match(pattern)
    if (match) return replacement(...match)
  }

  return null
}
